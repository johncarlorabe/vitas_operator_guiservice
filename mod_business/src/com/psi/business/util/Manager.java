package com.psi.business.util;

import com.tlc.gui.modules.common.Model;



public class Manager
  extends Model
{
  protected String email;
  protected String firstname;
  protected String lastname;
  protected String midname;
  protected String msisdn;
  protected String password;
  protected String branch;
  protected String manager;
  protected String username;
  protected String id;
  protected String accountnumber;
  protected String createdby;
  
  public String getCreatedby() { return this.createdby; }

  
  public void setCreatedby(String createdby) { this.createdby = createdby; }

  
  public String getEmail() { return this.email; }

  
  public void setEmail(String email) { this.email = email; }

  
  public String getFirstname() { return this.firstname; }

  
  public void setFirstname(String firstname) { this.firstname = firstname; }

  
  public String getLastname() { return this.lastname; }

  
  public void setLastname(String lastname) { this.lastname = lastname; }

  
  public String getMidname() { return this.midname; }

  
  public void setMidname(String midname) { this.midname = midname; }

  
  public String getMsisdn() { return this.msisdn; }

  
  public void setMsisdn(String msisdn) { this.msisdn = msisdn; }

  
  public String getPassword() { return this.password; }

  
  public void setPassword(String password) { this.password = password; }

  
  public String getBranch() { return this.branch; }

  
  public void setBranch(String branch) { this.branch = branch; }

  
  public String getManager() { return this.manager; }

  
  public void setManager(String manager) { this.manager = manager; }

  
  public String getUsername() { return this.username; }

  
  public void setUsername(String username) { this.username = username; }

  
  public String getId() { return this.id; }

  
  public void setId(String id) { this.id = id; }

  
  public String getAccountnumber() { return this.accountnumber; }

  
  public void setAccountnumber(String accountnumber) { this.accountnumber = accountnumber; }
}
